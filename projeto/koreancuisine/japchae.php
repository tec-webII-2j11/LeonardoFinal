					<h1>Japchae </h1>                
                    <a href ="bibimbap.html"><img src="photos/top4.jpg" width=250px></a>
					<br><br>
                    
                    <h3>Ingredientes</h3>
                    <li>30g de dangmyeon (espaguete de batata)</li>
					<li>110g de espinafre precozido</li>
					<li>cenoura, cortada em fatias finas</li>
					<li>cebola, cortada em fatias finas</li>
					<li>10g de cogumelos mogi</li>
					<li>110g de carne de boi</li>
					<li>Sementes de gergelim</li>
					<li>3 cogumelos shitake</li>
					<li>Sal a gosto</li>
					<li>1 ovo</li>

                    <br>
                    <h5>Tempero cogumelo</h5>
                    <li>2 colheres (ch&aacute;) de sementes de gergelim</li>
					<li>2 colheres (ch&aacute;) de &oacute;leo de gergelim</li>
					<li>2 colheres (sopa) de molho de soja</li>
                    <li>4 colheres (ch&aacute;) de cebola picada</li>
                    <li>2 colheres (ch&aacute;) de alho picado</li>
                    <li>1 colher (sopa) de a&ccedil;&uacute;car</li>
					<br>
                    <h5>Tempero espinafre</h5>
                    <li>2 colheres (ch&aacute;) de sementes de gergelim</li>
                    <li>2 colheres (ch&aacute;) de &oacute;leo de gergelim</li>
                    <li>2 colheres (ch&aacute;) de cebola picada</li>
                    <li>1 colher (ch&aacute;) de alho picado</li>
                    <li>Sal a gosto</li>
					<br>
                    <h5>Tempero espaguete</h5>
                    <li>1 colher (sopa) de molho de soja</li>
                    <li>1 colher (sopa) de &oacute;leo de soja</li>
                    <li>1 colher (sopa) de a&ccedil;&uacute;car</li>
					<br>
                    <h3>Modo de preparo</h3>
                    <li>Corte a carne de boi em fatias diagonais com cerca de 5cm de comprimento.</li>
                    <li>Reserve.</li>
                    <li>Deixe os cogumelos mogi e shiitake em &aacute;gua morna por 10 minutos.</li>
                    <li>Depois, escorra, corte em fatias finas e tempere.</li>
                    <li>Reserve.</li>
                    <li>Pique o espinafre em fatias, tamb&eacute;m, e tempere.</li>
                    <li>Ferva &aacute;gua em uma panela e cozinhe o espaguete at&eacute; ficar macio.</li>
                    <li>Lave em &aacute;gua fria e escorra bem.</li>
                    <li>Parta o espaguete em tr&ecirc;s partes e misture bem com o molho de soja, o a&ccedil;&uacute;car e o &oacute;leo.</li>
                    <li>Bata o ovo, frite como uma omelete e corte em tiras finas.</li>
                    <li>Em uma frigideira grande, frier rapidamente em um pouco de &oacute;leo, uma de cada vez, a cebola e a cenoura.</li>
                    <li>Salpique com um pouco de sal e reserve.</li>
                    <li>Na mesma frigideira, frite da mesma forma, a carne e os cogumelos.</li>
                    <li>Finalmente, frite o espaguete.</li>
                    <li>Adicione os demais ingredientes reservados e frite mais um pouco, mexendo bem.</li>
                    <li>Arrume o espaguete j&aacute; pronto nos pratos de servir e decore com as fatias de ovo.</li>
                    <li>Sirva imediatamente.</li>

