                    <h3>Ingredientes</h3>
                    <li>3 de arroz oriental</li>
                    <li>3 e ½ de água</li>
                    <li>½ cenoura, fatiada do tamanho de palitos de fósforo</li>
                    <li>110 gramas de carne de boi moída ou bem picada</li>
                    <li>1 pepino, cortado em rodelas de 2,5 mm</li>
                    <li>1 folha de muk (geléia vegetal) fatiado</li>
                    <li>pasta de pimenta vermelha temperada*</li>
                    <li>100 gramas de espinafre</li>
                    <li>50 gramas de brotos de feijão</li>
                    <li>1 folha de alface crespa</li>
                    <li>3 cogumelos shitake fatiados</li>
                    <li>4 ovos</li>
                    <br>
                    <h5>Pasta de pimenta temperada</h5>
                    <li>4 (sopa) de pimenta vermelha sem pele e sem sementes picada, passada pelo liquidificador ( Vende em mercado coreano ou japones pronto.)</li>
                    <li>1 (sopa) de sementes de gergelim</li>
                    <li>1 (chá) de óleo de gergelim</li>
                    <li>1 (sopa) de açúcar</li>
					<br>
                    <h3>Modo de preparo</h3>
                    <p>Lavar o arroz. Deixar de molho por 30 minutos e escorrer. Colocar em uma panela, adicionar 3 e ½ xícaras de água e levar ao fogo, sem tampar. Cerca de 10 a 15 minutos depois que ferver, reduzir a chama e cozinhar com a panela tampada por mais 5 minutos. Não destampar a panela durante o cozimento, pois fará o arroz desandar. Temperar a carne. Colocar um pouco de óleo em uma frigideira grande e fritar, mexendo sempre até cozinhar. Reservar. Salpicar o pepino, a cenoura e os cogumelos com sal. Deixar descansar por 5 minutos. Passado este tempo, escorrer a água que se formar. Temperar cada vegetal separadamente. Reservar. Levar ao fogo uma frigideira para esquentar, adicionar um pouco de óleo de gergelim e fritar rapidamente, mexendo bem, as rodelas de pepino até que sua cor fique bem viva. Retirar e colocar em um prato para que esfrie. Na mesma frigideira, refogar separadamente e da mesma forma o espinafre, a cenoura e os cogumelos consecutivamente. Em uma outra frigideira fritar os ovos em óleo não muito quente, sem virá-los, para que não se quebrem ou cozinhar demais as gemas. Reservar. Preparar a pasta de pimenta vermelha, misturando bem todos os ingredientes.</p>
                    <h3>Montagem Final</h3>
                    <p>Colocar o arroz em uma tigela de servir. Arrumar por cima os vegetais já preparados e as tiras de geléia. Enfeite com a alface. Finalizar colocando os ovos sobre o prato. Servir guarnecido com a pasta de pimenta.</p>

