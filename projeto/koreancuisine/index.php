					<h3>Saúde & Qualidade de Vida - Curiosidades</h3>
                    <p>A culinária coreana, ou Hansik é uma comida saudável que harmoniza diferentes ingredientes, criando receitas ricas e nutritivas, pois reúne grãos, como o arroz, legumes e carne de forma e oferecer um ótimo equilíbrio entre carboidratos, proteínas e gorduras.

Graças à sua posição geográfica, cercada por água em seus três lados, o povo corano teve fácil acesso a ingredientes da montanha, da fazenda, e do mar, o que permitiu uma grande variedade de ingredientes e criações. Além disso, cada produto é tem um valor imporante e um simbolismo nas receitas.</p>
					<p>Autêntica e rica, a culinária da Coréia soube preservar a sua identidade. Os invasores Japão e China não conseguiram mudar a culinária, pelo contrário. Os coreanos usaram a interferência para aperfeiçoar técnicas e inovar pratos. Em um primeiro olhar, ou prova de uma comida, se percebem algumas influências, mas as semelhanças cessam por aí.</p>
					<p>Os sabores e as combinações se desenvolveram com personalidade própria, cultura e história.</p>
                    <p>O prato mais representativo da peculiar culinária coreana, o kimchi, é uma conserva supercondimentada de vegetais, um componente básico da dieta local. Ele surgiu para substituir os vegetais frescos durante o rigor do inverno. Cada região do país tem a sua receita de kimchi, e ao todo existem mais de 160 variações. A conserva feita com acelga é a mais conhecida e consumida. Não há pedaço de terra cultivável na Coréia que não tenha uma plantação de acelga ou de arroz, que também não falta nunca à mesa.</p>
                    <p>Segundo a tradição, em uma mesa coreana não podem faltar três cores: o branco, geralmente representado pelo arroz; o vermelho, que denuncia a pimenta; e o verde, sempre presente nas verduras. A ancestral receita do kimchi simboliza o gosto peculiar por alho – dizem que os coreanos até exalam alho --, e por pimenta vermelha.</p>
                    <p>A Coréia é banhada por oceanos de todos os lados, por isso dispõe de uma grande variedade de peixes e de frutos do mar. Eles são usados em sopas, assados e caldeiradas ou são secados e salgados para emprego como matéria-prima na cozinha. O frescor dos peixes também rende o hoe ou peixe cru fatiado, mas que leva no lugar do wasabi (raiz-forte) uma forte mistura de pasta de pimenta temperada com vinagre e açúcar.</p>
