					<h1>Bulgogui </h1>                
                    <a href ="bulgogui.html"><img src="photos/top3.jpg" width=250px></a>
					<br><br>
                    
                    <h3>Ingredientes</h3>
                    <li>700 gramas de filé mignon ou contrafilé</li>
                    <li>folhas de alface e de gergelim frescas</li>
                    <li>pasta de soja (missô) temperada</li>                    <br>
                    <h5>Para Marinada da carne</h5>
                    <li>5 (sopa) de cebolinha verde bem picada, em diagonal</li>
                    <li>1 (chá) de sementes de gergelim torradas</li>
                    <li>1 (chá) de glutamato monossódico</li>
                    <li>¼ (chá) de pimenta-do-reino</li>
                    <li>1 (sopa) de óleo de gergelim</li>
                    <li>1 (sopa) de molho de soja</li>
                    <li>1 (chá) de alho moído</li>
                    <li>1 (sopa) de açúcar</li>
					<br>
                    <h5>Pasta de soja temperada</h5>
                    <li>1 (sopa) de cebolinha verde bem picada</li>
                    <li>1 (sopa) de pimentão verde bem picado</li>
                    <li>1 (sopa) de sementes de gergelim</li>
                    <li>1 (sopa) de molho de pimenta</li>
                    <li>½ (chá) de óleo de gergelim</li>
                    <li>2 (sopa) de cebola picada</li>
                    <li>½ (chá) de alho picado</li>
                    <li>½ (sopa) de açúcar</li>
                    <li>½ de água</li>
                    <li>100 gramas de pasta de soja (missô)</li>
                    <li>60 gramas de carne de boi moída</li><br>
                    <h3>Modo de preparo</h3>
                    <p>Cortar a carne em fatias bem finas. Misturar todos os ingredientes da marinada, colocar as fatias de carne e deixar tomar gosto por 1 hora. Lavar as folhas de alface e de gergelim. Escorrer e deixar secar bem. Misturar todos os ingredientes da pasta de soja. Levar ao fogo rapidamente e deixar até ferver; desligar e esperar esfriar. Fritar rapidamente as fatias de carne em um pouco de óleo em uma frigideira. Levar a carne, junto com todos os outros ingredientes à mesa. Cada convidado coloca, em folhas de alface ou gergelim, um pouco da carne, a pasta de soja, arroz cozido e um pouco de kimchi. Formam-se trouxinhas que vão sendo degustadas aos bocados. O bulgogui pode ser acompanhado por vários tipos de legumes salteados ou outras conservas, além do kimchi. A carne, o arroz e o kimtchi não precisam ser necessariamente degustados com a alface, podem ser comidos diretamente com o hashi. Os coreanos gostam de colocar pequenas porções da carne e dos acompanhamentos sobre o arroz e comer tudo com uma colher. </p>
                    

